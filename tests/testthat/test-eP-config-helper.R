test_that("No labpreset works", {
  expect_equal(eP.config.helper(labpreset = FALSE,
                                blankKey = "Blank",
                                sep = ";",
                                dec = ",",
                                colPol = 6,
                                colL = 17,
                                colReqPol = 24,
                                ReqPolKey = "ja",
                                colShape = 25,
                                colCol = 26,
                                colLFib = 27,
                                fibre = "Faser",
                                sphere = "Kugel",
                                fragment = "Fragment",
                                pixel = "Pixel"), 
               data.frame(
                 labname = FALSE,
                 blankKey = "Blank",
                 sep = ";",
                 dec = ",",
                 colPol = 6,
                 colL = 17,
                 colReqPol = 24,
                 ReqPolKey = "ja",
                 colShape = 25,
                 colCol = 26,
                 colLFib = 27,
                 fibre = "Faser",
                 sphere = "Kugel",
                 fragment = "Fragment",
                 pixel = "Pixel"
               ))
})


